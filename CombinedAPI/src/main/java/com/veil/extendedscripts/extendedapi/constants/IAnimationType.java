package com.veil.extendedscripts.extendedapi.constants;

/**
 * This object stores animation types available to all scripting handlers through the "AnimationType" keyword.
 */
public interface IAnimationType {
    int NONE = 0;
    int SITTING = 1;
    int LYING = 2;
    int SNEAKING = 3;
    int DANCING = 4;
    int AIMING = 5;
    int CRAWLING = 6;
    int HUGGING = 7;
}
